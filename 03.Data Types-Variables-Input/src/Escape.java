public class Escape {
    public static void main(String[] args) {
        System.out.println("Welcome to \"JAVA\" Coding");
        System.out.println("Welcome to \\JAVA\\ Coding");
        System.out.println("Welcome to \bJAVA\b Coding");
        System.out.println("Welcome to JAVA \t Coding");
        System.out.println("Welcome to \"JAVA\" Coding");
        System.out.println("Welcome to \'JAVA\' Coding");
        System.out.println("Welcome\n to \"JAVA\" Coding");
        System.out.println("*****\n****\n***\n**\n*");
        System.out.println("*\t*\t*\t*\t*\n*\t*\t*\t*\n*\t*\t*\n*\t*\n*");
        System.out.println("\n\n     *\n   **\n  ***\n ****\n*****");
    }
}
