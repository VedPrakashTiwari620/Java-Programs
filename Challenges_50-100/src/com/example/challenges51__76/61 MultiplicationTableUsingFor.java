package com.example.challenges51__76;

class MultiplicationTableUsingFor {
    public static void main(String[] args) {
        System.out.println("Welcome to print multiplication table");


        for (int i=1;i<=20;i++){
            for (int j=1;j<=10;j++){
                System.out.println(i+"x"+j+"="+i*j);
            }
        }
    }
}
